def computer_guess_game():
    print("Загадайте число от 1 до 100. Я попробую его угадать!")
    low = 1
    high = 100
    attempts = 0
    feedback = ''

    while feedback != 'у': # 'у' - угадал
        attempts += 1
        guess = (low + high) // 2 # Выбираем среднее число в текущем диапазоне
        print(f"Моя догадка: {guess}")
        feedback = input("Ваше число (б - больше, м - меньше, у - угадал): ").lower()

        if feedback == 'б': # Если загаданное число больше догадки
            low = guess + 1
        elif feedback == 'м': # Если загаданное число меньше догадки
            high = guess - 1
        elif feedback == 'у': # Если компьютер угадал
            print(f"Ура! Я угадал ваше число {guess} за {attempts} попыток.")
        else:
            print("Некорректный ввод. Используйте 'б', 'м' или 'у'.")
            attempts -= 1 # Не увеличиваем счетчик попыток при некорректном вводе

        if low > high: # Ситуация, когда пользователь мог ошибиться или обмануть
            print("Кажется, вы меня обманываете или допустили ошибку. Числа в этом диапазоне больше нет.")
            break

# Для демонстрации раскомментируйте вызов:
computer_guess_game()
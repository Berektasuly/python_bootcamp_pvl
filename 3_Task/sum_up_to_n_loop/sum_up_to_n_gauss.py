def sum_up_to_n_gauss():
    # Знаете ли вы, что для этой задачи не нужен цикл? Есть формула Гаусса: n * (n + 1) / 2
    # Эта формула работает, потому что она симметрично суммирует пары чисел: (1+N), (2+N-1) и т.д.
    try:
        n = int(input("Введите целое число N: "))
        if n < 0:
            print("N должно быть неотрицательным числом.")
            return

        total_sum = n * (n + 1) // 2 # Используем целочисленное деление //
        print(f"Сумма чисел от 1 до {n} (по формуле) равна: {total_sum}")
    except ValueError:
        print("Некорректный ввод. Пожалуйста, введите целое число.")

# Для демонстрации раскомментируйте вызов:
sum_up_to_n_gauss()